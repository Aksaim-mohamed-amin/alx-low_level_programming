#include "operations.h"

/**
 * add - Add two numbers.
 * @a: First number.
 * @b: Secend number.
 *
 * Return: a + b.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Substract two numbers.
 * @a: First number.
 * @b: Secend number.
 *
 * Return: a - b.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiply two numbers.
 * @a: First number.
 * @b: Secend number.
 *
 * Return: a * b.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divisie two numbers.
 * @a: First number.
 * @b: Secend number.
 *
 * Return: a / b.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Calculate the modular of two numbers.
 * @a: First number.
 * @b: Secend number.
 *
 * Return: a % b.
 */
int mod(int a, int b)
{
	return (a % b);
}
